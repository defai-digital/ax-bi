#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct LocalRuntimeStatus {
    pub runtime_dir: String,
    pub compose_file: String,
    pub env_file: String,
    pub configured: bool,
    pub dependencies: Vec<LocalRuntimeDependency>,
    pub ports: Vec<LocalRuntimePort>,
    /// OS label: `macos`, `windows`, or `linux`.
    pub platform: String,
    /// Always true on desktop builds — both macOS and Windows can run local AX BI Docker.
    pub local_runtime_supported: bool,
    /// True when required CLI tools are installed (engine may still need to start).
    pub can_start_local: bool,
    /// Engine identifier: `colima` (macOS) or `docker` (Windows/Linux).
    pub engine_name: String,
    /// UI label for the VM/engine layer (e.g. "Colima", "Docker Engine").
    pub engine_label: String,
    /// Whether the container engine is ready enough to run Compose.
    pub engine_running: bool,
    /// Colima profile name on macOS; empty on other platforms.
    pub colima_profile: String,
    /// Backward-compatible alias of engine readiness for Colima-era clients.
    pub colima_running: bool,
    pub docker_host: String,
    pub docker_ready: bool,
    /// Whether any service in the managed Compose project is running.
    pub stack_running: bool,
    /// Whether the managed AX BI web service is running.
    pub axbi_running: bool,
    pub axbi_healthy: bool,
    pub web_url: String,
    pub mcp_url: String,
    pub services_url: String,
    pub admin_username: String,
    pub admin_password_present: bool,
    pub onboarding_required: bool,
    /// Desktop package version (`CARGO_PKG_VERSION`).
    pub desktop_version: String,
    /// Configured AX BI container image (from managed `.env` when present).
    pub axbi_image: String,
    /// Configured AX Services container image (from managed `.env` when present).
    pub ax_services_image: String,
    /// Recommended engine CPUs for local mode (Colima profile / host guidance).
    pub recommended_cpus: u16,
    /// Recommended engine memory in GiB for local mode.
    pub recommended_memory_gb: u16,
}

