/// True when `image` is a DEFAI-managed registry ref we are allowed to re-pin.
fn is_managed_image_ref(image: &str, repo: &str) -> bool {
    let image = image.trim();
    if image.is_empty() {
        return true;
    }
    if image == repo {
        return true;
    }
    let prefix = format!("{repo}:");
    if let Some(tag) = image.strip_prefix(&prefix) {
        // Digests (`repo@sha256:…`) are treated as custom pins.
        return !tag.is_empty() && !image.contains('@');
    }
    false
}

