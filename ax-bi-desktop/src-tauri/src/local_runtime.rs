/// Fail fast with install hints when CLI tools are missing (before starting engines).
fn ensure_cli_dependencies() -> Result<(), String> {
    let missing: Vec<LocalRuntimeDependency> = dependencies()
        .into_iter()
        .filter(|dependency| !dependency.installed)
        .collect();
    if missing.is_empty() {
        return Ok(());
    }

    let details = missing
        .iter()
        .map(|dependency| format!("- {}: {}", dependency.name, dependency.install_hint))
        .collect::<Vec<_>>()
        .join("\n");

    Err(format!(
        "Cannot start local AX BI Docker on {} — missing tools:\n{}\n\n\
         Run locally uses the same AX BI Compose stack on macOS and Windows \
         (Colima on Mac, Docker Desktop on Windows).",
        platform_label(),
        details
    ))
}

fn format_compose_error(action: &str, error: &str, runtime_dir: Option<&Path>) -> String {
    let diagnostics = runtime_dir
        .map(compose_failure_diagnostics)
        .unwrap_or_default();
    format!(
        "Failed to {action} the local AX BI Docker stack on {}.\n\
         Engine: {} ({})\n\
         {}{}\n\n\
         Tips:\n\
         - Open Settings > Advanced runtime to inspect service logs\n\
         - Stop other applications using the configured AX BI ports, then retry\n\
         - macOS: confirm Colima is running; Windows: confirm Docker Desktop is running",
        platform_label(),
        RuntimeEngine::current().label(),
        RuntimeEngine::current().name(),
        bounded_output_tail(error, 80, 12_000),
        diagnostics,
    )
}

/// Collect bounded service state and logs after a Compose command fails.
fn compose_failure_diagnostics(runtime_dir: &Path) -> String {
    let mut diagnostics = String::new();
    if let Ok(output) = run_docker_compose(runtime_dir, &["ps", "--all"]) {
        diagnostics.push_str(&format_output(
            "compose service state",
            &bounded_output_tail(&join_outputs(&output.stdout, &output.stderr), 80, 8_000),
        ));
    }
    if let Ok(output) = run_docker_compose(
        runtime_dir,
        &[
            "logs",
            "--no-color",
            "--tail",
            "80",
            "ax-bi-init",
            "ax-bi",
            "mcp",
            "ax-services",
        ],
    ) {
        diagnostics.push_str(&format_output(
            "recent startup logs",
            &bounded_output_tail(&join_outputs(&output.stdout, &output.stderr), 160, 16_000),
        ));
    }
    diagnostics
}

/// Keep the actionable tail of noisy subprocess output within UI-safe bounds.
fn bounded_output_tail(value: &str, max_lines: usize, max_chars: usize) -> String {
    let normalized = value.replace('\r', "\n");
    let lines: Vec<&str> = normalized.lines().collect();
    let start = lines.len().saturating_sub(max_lines);
    let mut tail = lines[start..].join("\n");
    let char_count = tail.chars().count();
    if char_count > max_chars {
        tail = tail
            .chars()
            .skip(char_count - max_chars)
            .collect::<String>();
    }
    tail.trim().to_string()
}

fn dependency(
    name: &str,
    command: &str,
    args: &[&str],
    install_hint: &str,
) -> LocalRuntimeDependency {
    match run_command(command, args, None, &[]) {
        Ok(output) => LocalRuntimeDependency {
            name: name.to_string(),
            command: if args.is_empty() {
                command.to_string()
            } else {
                format!("{command} {}", args.join(" "))
            },
            resolved_path: path_to_string(&resolve_program(command)),
            installed: true,
            version: first_line(&join_outputs(&output.stdout, &output.stderr)),
            install_hint: install_hint.to_string(),
        },
        Err(_) => LocalRuntimeDependency {
            name: name.to_string(),
            command: command.to_string(),
            resolved_path: path_to_string(&resolve_program(command)),
            installed: false,
            version: None,
            install_hint: install_hint.to_string(),
        },
    }
}

fn first_line(value: &str) -> Option<String> {
    value.lines().next().map(str::trim).and_then(|line| {
        if line.is_empty() {
            None
        } else {
            Some(line.to_string())
        }
    })
}

/// Ensure the platform container engine is running.
fn ensure_engine(runtime_dir: &Path) -> Result<CommandOutput, String> {
    match RuntimeEngine::current() {
        RuntimeEngine::Colima => {
            run_colima_start(&configured_colima_profile(&env_file(runtime_dir)))
        }
        RuntimeEngine::Docker => ensure_docker_engine(runtime_dir),
    }
}

fn run_colima_start(profile: &str) -> Result<CommandOutput, String> {
    // Explicit limactl check gives a clearer error than Colima's PATH fatality.
    let limactl = resolve_program("limactl");
    if !limactl.is_file() {
        return Err("Lima (limactl) was not found. Colima requires Lima.\n\
             Install with: brew install lima colima\n\
             If already installed, ensure /opt/homebrew/bin is available."
            .to_string());
    }

    let mut prior_output = CommandOutput {
        stdout: String::new(),
        stderr: String::new(),
    };
    let docker_host = docker_host(profile)?;
    if docker_daemon_ready(&docker_host) {
        // Colima's CLI status path can take several seconds. Its generated
        // profile is enough to validate resources once Docker is responsive.
        if colima_resources_sufficient(profile).unwrap_or(true) {
            return Ok(CommandOutput {
                stdout: format!(
                    "Colima profile {profile} is already running with sufficient resources"
                ),
                stderr: String::new(),
            });
        }
        prior_output = run_command("colima", &["stop", "--profile", profile], None, &[])?;
    }

    let start_output = run_command(
        "colima",
        &[
            "start",
            "--profile",
            profile,
            "--runtime",
            "docker",
            "--cpu",
            AXBI_COLIMA_CPUS,
            "--memory",
            AXBI_COLIMA_MEMORY_GB,
            "--activate=false",
        ],
        None,
        &[],
    )?;
    Ok(CommandOutput {
        stdout: join_outputs(&prior_output.stdout, &start_output.stdout),
        stderr: join_outputs(&prior_output.stderr, &start_output.stderr),
    })
}

fn colima_resources_sufficient(profile: &str) -> Option<bool> {
    if !valid_colima_profile(profile) {
        return None;
    }
    let config = fs::read_to_string(
        home_dir()
            .ok()?
            .join(".colima")
            .join(profile)
            .join("colima.yaml"),
    )
    .ok()?;
    colima_config_resources_sufficient(&config)
}

/// Parse top-level Colima CPU and memory settings without invoking its slow CLI.
fn colima_config_resources_sufficient(config: &str) -> Option<bool> {
    let mut cpu = None;
    let mut memory = None;
    for line in config.lines() {
        if line.chars().next().is_some_and(char::is_whitespace) {
            continue;
        }
        let Some((key, value)) = line.split_once(':') else {
            continue;
        };
        match key.trim() {
            "cpu" => cpu = value.trim().parse::<u64>().ok(),
            "memory" => memory = value.trim().parse::<u64>().ok(),
            _ => {}
        }
    }
    let required_cpu = AXBI_COLIMA_CPUS.parse::<u64>().unwrap_or(4);
    let required_memory = AXBI_COLIMA_MEMORY_GB.parse::<u64>().unwrap_or(8);
    Some(cpu? >= required_cpu && memory? >= required_memory)
}

fn ensure_docker_engine(runtime_dir: &Path) -> Result<CommandOutput, String> {
    let docker_host = docker_host(&configured_colima_profile(&env_file(runtime_dir)))?;
    if docker_daemon_ready(&docker_host) {
        return Ok(CommandOutput {
            stdout: format!(
                "Docker engine already running on {} ({docker_host})",
                platform_label()
            ),
            stderr: String::new(),
        });
    }

    // Launch Docker Desktop when the CLI exists but the daemon is down.
    if let Some(desktop) = docker_desktop_app_path() {
        log::info!("Starting Docker Desktop at {}", desktop.display());
        let mut command = Command::new(&desktop);
        configure_subprocess(&mut command);
        command.spawn().map_err(|error| {
            format!(
                "Docker Engine is not running and Docker Desktop failed to start: {error}\n{}",
                docker_install_hint()
            )
        })?;

        if wait_for_docker_daemon(&docker_host, DOCKER_ENGINE_WAIT)? {
            return Ok(CommandOutput {
                stdout: format!(
                    "Started Docker Desktop on {} and waited for the engine",
                    platform_label()
                ),
                stderr: String::new(),
            });
        }

        return Err(format!(
            "Docker Desktop was launched on {} but the engine did not become ready in time.\n\
             Open Docker Desktop, wait until it reports Running, then try Run locally again.\n\
             Expected Docker host: {docker_host}",
            platform_label()
        ));
    }

    Err(format!(
        "Docker Engine is not available on {}.\n{}",
        platform_label(),
        docker_install_hint()
    ))
}

fn wait_for_docker_daemon(docker_host: &str, timeout: Duration) -> Result<bool, String> {
    let deadline = Instant::now() + timeout;
    while Instant::now() < deadline {
        if docker_daemon_ready(docker_host) {
            return Ok(true);
        }
        thread::sleep(DOCKER_ENGINE_POLL);
    }
    Ok(false)
}

fn docker_daemon_ready(docker_host: &str) -> bool {
    let envs = docker_host_envs(docker_host);
    let env_refs: Vec<(&str, &str)> = envs.iter().map(|(k, v)| (k.as_str(), v.as_str())).collect();
    run_command("docker", &["info"], None, &env_refs).is_ok()
}

fn run_docker_compose(runtime_dir: &Path, compose_args: &[&str]) -> Result<CommandOutput, String> {
    run_docker_compose_owned(
        runtime_dir,
        compose_args.iter().map(ToString::to_string).collect(),
    )
}

fn run_docker_compose_owned(
    runtime_dir: &Path,
    compose_args: Vec<String>,
) -> Result<CommandOutput, String> {
    let compose_path = compose_file(runtime_dir);
    let env_path = env_file(runtime_dir);
    let mut args = vec![
        "compose".to_string(),
        "--project-name".to_string(),
        AXBI_COMPOSE_PROJECT.to_string(),
        "--env-file".to_string(),
        path_to_string(&env_path),
        "-f".to_string(),
        path_to_string(&compose_path),
    ];
    args.extend(compose_args);
    let docker_host = docker_host(&configured_colima_profile(&env_file(runtime_dir)))?;
    let envs = docker_host_envs(&docker_host);
    let env_refs: Vec<(&str, &str)> = envs.iter().map(|(k, v)| (k.as_str(), v.as_str())).collect();
    let arg_refs: Vec<&str> = args.iter().map(String::as_str).collect();
    run_command("docker", &arg_refs, Some(runtime_dir), &env_refs)
}

/// Start Compose and reconcile transient command failures against final health.
fn start_compose_stack(runtime_dir: &Path) -> Result<CommandOutput, String> {
    match run_docker_compose(runtime_dir, &["up", "-d", "--remove-orphans"]) {
        Ok(output) => {
            wait_for_runtime_health(runtime_dir)
                .map_err(|error| format_compose_error("start", &error, Some(runtime_dir)))?;
            Ok(output)
        }
        Err(error) => {
            if wait_for_runtime_health_for(runtime_dir, COMPOSE_FAILURE_RECONCILE_WAIT).is_ok() {
                return Ok(CommandOutput {
                    stdout:
                        "Compose reported a transient startup failure, but AX BI became healthy"
                            .to_string(),
                    stderr: bounded_output_tail(&error, 40, 6_000),
                });
            }
            Err(format_compose_error("start", &error, Some(runtime_dir)))
        }
    }
}

fn compose_services_running(runtime_dir: &Path) -> Result<bool, String> {
    Ok(compose_running_services(runtime_dir)?.contains("ax-bi"))
}

/// Return the set of running services owned by the managed Compose project.
fn compose_running_services(runtime_dir: &Path) -> Result<HashSet<String>, String> {
    if !compose_file(runtime_dir).exists() || !env_file(runtime_dir).exists() {
        return Ok(HashSet::new());
    }
    let output = run_docker_compose_owned(
        runtime_dir,
        vec![
            "ps".to_string(),
            "--status".to_string(),
            "running".to_string(),
            "--services".to_string(),
        ],
    )?;

    Ok(output
        .stdout
        .lines()
        .map(str::trim)
        .filter(|line| !line.is_empty())
        .map(ToString::to_string)
        .collect())
}

fn wait_for_runtime_health(runtime_dir: &Path) -> Result<(), String> {
    wait_for_runtime_health_for(runtime_dir, STARTUP_HEALTH_RECONCILE_WAIT)
}

/// Wait up to the supplied timeout for the managed web service to become healthy.
fn wait_for_runtime_health_for(runtime_dir: &Path, timeout: Duration) -> Result<(), String> {
    let deadline = Instant::now() + timeout;
    let mut last_ps = String::new();
    let urls = runtime_urls(&env_file(runtime_dir));

    while Instant::now() < deadline {
        if compose_services_running(runtime_dir).unwrap_or(false)
            && local_http_healthcheck(urls.web_port)
        {
            return Ok(());
        }

        if let Ok(output) = run_docker_compose(runtime_dir, &["ps"]) {
            last_ps = join_outputs(&output.stdout, &output.stderr);
        }
        thread::sleep(STARTUP_HEALTH_RECONCILE_POLL);
    }

    Err(format!(
        "AX BI containers started, but the web app did not become healthy within {} seconds.{}",
        timeout.as_secs(),
        format_output("compose ps", &last_ps)
    ))
}

