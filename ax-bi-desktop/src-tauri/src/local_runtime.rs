#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct LocalRuntimeDependency {
    pub name: String,
    pub command: String,
    pub installed: bool,
    pub version: Option<String>,
    pub install_hint: String,
}

#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct LocalRuntimeStatus {
    pub runtime_dir: String,
    pub compose_file: String,
    pub env_file: String,
    pub configured: bool,
    pub dependencies: Vec<LocalRuntimeDependency>,
    /// OS label: `macos`, `windows`, or `linux`.
    pub platform: String,
    /// Always true on desktop builds — both macOS and Windows can run local AX BI Docker.
    pub local_runtime_supported: bool,
    /// True when required CLI tools are installed (engine may still need to start).
    pub can_start_local: bool,
    /// Engine identifier: `colima` (macOS) or `docker` (Windows/Linux).
    pub engine_name: String,
    /// UI label for the VM/engine layer (e.g. "Colima", "Docker Engine").
    pub engine_label: String,
    /// Whether the container engine is ready enough to run Compose.
    pub engine_running: bool,
    /// Colima profile name on macOS; empty on other platforms.
    pub colima_profile: String,
    /// Backward-compatible alias of engine readiness for Colima-era clients.
    pub colima_running: bool,
    pub docker_host: String,
    pub docker_ready: bool,
    pub axbi_running: bool,
    pub axbi_healthy: bool,
    pub web_url: String,
    pub mcp_url: String,
    pub services_url: String,
    pub admin_username: String,
    pub admin_password_present: bool,
}

#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct LocalRuntimeCommandOutput {
    pub status: LocalRuntimeStatus,
    pub stdout: String,
    pub stderr: String,
}

#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct LocalAdminCredentials {
    pub username: String,
    pub password: String,
}

#[derive(Debug)]
struct CommandOutput {
    stdout: String,
    stderr: String,
}

#[derive(Debug, Clone, Copy, PartialEq, Eq)]
enum RuntimeEngine {
    Colima,
    Docker,
}

impl RuntimeEngine {
    fn current() -> Self {
        if cfg!(target_os = "macos") {
            Self::Colima
        } else {
            Self::Docker
        }
    }

    fn name(self) -> &'static str {
        match self {
            Self::Colima => "colima",
            Self::Docker => "docker",
        }
    }

    fn label(self) -> &'static str {
        match self {
            Self::Colima => "Colima",
            Self::Docker => "Docker Engine",
        }
    }
}

pub fn status<R: Runtime>(app: &AppHandle<R>) -> Result<LocalRuntimeStatus, String> {
    log::info!("Checking local AX BI runtime status");
    let runtime_dir = runtime_dir(app)?;
    let compose_file = compose_file(&runtime_dir);
    let env_file = env_file(&runtime_dir);
    log::info!("Reading local runtime dependencies");
    let dependencies = dependencies();
    let configured = compose_file.exists() && env_file.exists();
    if configured {
        log::info!("Syncing generated local runtime files");
        sync_runtime_files(&compose_file, &env_file)?;
    }

    let engine = RuntimeEngine::current();
    log::info!("Checking {} status", engine.label());
    let engine_running = engine_is_running();
    let docker_host = docker_host()?;
    log::info!("Checking Docker status");
    let docker_ready = docker_daemon_ready(&docker_host);
    log::info!("Checking Compose service status");
    let axbi_running = docker_ready && compose_services_running(&runtime_dir)?;
    log::info!("Checking AX BI HTTP health");
    let axbi_healthy = axbi_running && local_http_healthcheck();
    let admin_password_present = configured
        && read_env_value(&env_file, "ADMIN_PASSWORD")
            .ok()
            .flatten()
            .map(|password| !password.is_empty())
            .unwrap_or(false);

    let colima_profile = match engine {
        RuntimeEngine::Colima => AXBI_COLIMA_PROFILE.to_string(),
        RuntimeEngine::Docker => String::new(),
    };
    let can_start_local = dependencies.iter().all(|dependency| dependency.installed);

    Ok(LocalRuntimeStatus {
        runtime_dir: path_to_string(&runtime_dir),
        compose_file: path_to_string(&compose_file),
        env_file: path_to_string(&env_file),
        configured,
        dependencies,
        platform: platform_id().to_string(),
        local_runtime_supported: true,
        can_start_local,
        engine_name: engine.name().to_string(),
        engine_label: engine.label().to_string(),
        engine_running,
        colima_profile,
        // Keep Colima-era field name; UI uses engine_running / engine_label.
        colima_running: engine_running,
        docker_host,
        docker_ready,
        axbi_running,
        axbi_healthy,
        web_url: AXBI_WEB_URL.to_string(),
        mcp_url: AXBI_MCP_URL.to_string(),
        services_url: AXBI_SERVICES_URL.to_string(),
        admin_username: AXBI_ADMIN_USERNAME.to_string(),
        admin_password_present,
    })
}

pub fn prepare<R: Runtime>(app: &AppHandle<R>) -> Result<LocalRuntimeStatus, String> {
    ensure_runtime_files(app)?;
    status(app)
}

pub fn start<R: Runtime>(app: &AppHandle<R>) -> Result<LocalRuntimeCommandOutput, String> {
    let runtime_dir = ensure_runtime_files(app)?;
    ensure_cli_dependencies()?;
    let engine_output = ensure_engine()?;
    let compose_output = run_docker_compose(&runtime_dir, ["up", "-d", "--remove-orphans"])
        .map_err(|error| format_compose_error("start", &error))?;

    Ok(LocalRuntimeCommandOutput {
        status: status(app)?,
        stdout: join_outputs(&engine_output.stdout, &compose_output.stdout),
        stderr: join_outputs(&engine_output.stderr, &compose_output.stderr),
    })
}

pub fn stop<R: Runtime>(app: &AppHandle<R>) -> Result<LocalRuntimeCommandOutput, String> {
    let runtime_dir = ensure_runtime_files(app)?;
    // Stop should still try even if the engine is flaky; surface compose errors clearly.
    let output = run_docker_compose(&runtime_dir, ["stop"])
        .map_err(|error| format_compose_error("stop", &error))?;

    Ok(LocalRuntimeCommandOutput {
        status: status(app)?,
        stdout: output.stdout,
        stderr: output.stderr,
    })
}

pub fn restart<R: Runtime>(app: &AppHandle<R>) -> Result<LocalRuntimeCommandOutput, String> {
    let runtime_dir = ensure_runtime_files(app)?;
    ensure_cli_dependencies()?;
    let engine_output = ensure_engine()?;
    let stop_output = run_docker_compose(&runtime_dir, ["stop"])
        .map_err(|error| format_compose_error("stop", &error))?;
    let start_output = run_docker_compose(&runtime_dir, ["up", "-d", "--remove-orphans"])
        .map_err(|error| format_compose_error("start", &error))?;

    Ok(LocalRuntimeCommandOutput {
        status: status(app)?,
        stdout: join_outputs(
            &join_outputs(&engine_output.stdout, &stop_output.stdout),
            &start_output.stdout,
        ),
        stderr: join_outputs(
            &join_outputs(&engine_output.stderr, &stop_output.stderr),
            &start_output.stderr,
        ),
    })
}

pub fn update<R: Runtime>(app: &AppHandle<R>) -> Result<LocalRuntimeCommandOutput, String> {
    let runtime_dir = ensure_runtime_files(app)?;
    ensure_cli_dependencies()?;
    let engine_output = ensure_engine()?;
    let pull_output = run_docker_compose(&runtime_dir, ["pull"])
        .map_err(|error| format_compose_error("pull", &error))?;
    let start_output = run_docker_compose(&runtime_dir, ["up", "-d", "--remove-orphans"])
        .map_err(|error| format_compose_error("start", &error))?;

    Ok(LocalRuntimeCommandOutput {
        status: status(app)?,
        stdout: join_outputs(
            &join_outputs(&engine_output.stdout, &pull_output.stdout),
            &start_output.stdout,
        ),
        stderr: join_outputs(
            &join_outputs(&engine_output.stderr, &pull_output.stderr),
            &start_output.stderr,
        ),
    })
}

pub fn logs<R: Runtime>(
    app: &AppHandle<R>,
    service: Option<String>,
    tail: Option<u16>,
) -> Result<String, String> {
    let runtime_dir = runtime_dir(app)?;
    if !compose_file(&runtime_dir).exists() || !env_file(&runtime_dir).exists() {
        return Err("Local AX BI runtime has not been prepared".to_string());
    }

    let mut args = vec![
        "logs".to_string(),
        "--no-color".to_string(),
        "--tail".to_string(),
        tail_lines(tail).to_string(),
    ];
    if let Some(service_name) = service {
        let service_name = validate_log_service(&service_name)?;
        args.push(service_name.to_string());
    }

    let output = run_docker_compose_owned(&runtime_dir, args)?;
    Ok(join_outputs(&output.stdout, &output.stderr))
}

pub fn credentials<R: Runtime>(app: &AppHandle<R>) -> Result<LocalAdminCredentials, String> {
    let env_file = env_file(&runtime_dir(app)?);
    let password = read_env_value(&env_file, "ADMIN_PASSWORD")?
        .filter(|value| !value.is_empty())
        .unwrap_or_else(|| AXBI_ADMIN_PASSWORD.to_string());

    Ok(LocalAdminCredentials {
        username: AXBI_ADMIN_USERNAME.to_string(),
        password,
    })
}

fn ensure_runtime_files<R: Runtime>(app: &AppHandle<R>) -> Result<PathBuf, String> {
    let runtime_dir = runtime_dir(app)?;
    fs::create_dir_all(&runtime_dir)
        .map_err(|error| format!("Failed to create local runtime directory: {error}"))?;

    let compose_path = compose_file(&runtime_dir);
    let env_path = env_file(&runtime_dir);
    sync_runtime_files(&compose_path, &env_path)?;

    Ok(runtime_dir)
}

fn sync_runtime_files(compose_path: &Path, env_path: &Path) -> Result<(), String> {
    fs::write(compose_path, LOCAL_COMPOSE_YAML)
        .map_err(|error| format!("Failed to write local Compose file: {error}"))?;

    if !env_path.exists() {
        fs::write(env_path, build_env_file()?)
            .map_err(|error| format!("Failed to write local runtime environment: {error}"))?;
    } else {
        ensure_runtime_secret(env_path)?;
    }

    Ok(())
}

fn runtime_dir<R: Runtime>(app: &AppHandle<R>) -> Result<PathBuf, String> {
    app.path()
        .app_data_dir()
        .map(|path| path.join(LOCAL_RUNTIME_DIR))
        .map_err(|error| format!("Failed to resolve app data directory: {error}"))
}

fn compose_file(runtime_dir: &Path) -> PathBuf {
    runtime_dir.join(LOCAL_COMPOSE_FILE)
}

fn env_file(runtime_dir: &Path) -> PathBuf {
    runtime_dir.join(LOCAL_ENV_FILE)
}

