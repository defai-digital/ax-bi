#[derive(Debug, Clone)]
struct RuntimeUrls {
    web_port: u16,
    mcp_port: u16,
    services_port: u16,
    web_url: String,
    mcp_url: String,
    services_url: String,
}

#[derive(Debug, Clone, Copy, PartialEq, Eq)]
enum RuntimeEngine {
    Colima,
    Docker,
}

impl RuntimeEngine {
    fn current() -> Self {
        if cfg!(target_os = "macos") {
            Self::Colima
        } else {
            Self::Docker
        }
    }

    fn name(self) -> &'static str {
        match self {
            Self::Colima => "colima",
            Self::Docker => "docker",
        }
    }

    fn label(self) -> &'static str {
        match self {
            Self::Colima => "Colima",
            Self::Docker => "Docker Engine",
        }
    }
}

pub fn status<R: Runtime>(app: &AppHandle<R>) -> Result<LocalRuntimeStatus, String> {
    log::info!("Checking local AX BI runtime status");
    let runtime_dir = runtime_dir(app)?;
    let compose_file = compose_file(&runtime_dir);
    let env_file = env_file(&runtime_dir);
    log::info!("Reading local runtime dependencies");
    let dependencies = dependencies();
    let urls = runtime_urls(&env_file);
    // Status is intentionally read-only: never rewrite compose/.env here.
    // Mutations happen in prepare/start/restart/update via ensure_runtime_files.
    let configured = compose_file.exists() && env_file.exists();

    let engine = RuntimeEngine::current();
    let colima_profile = configured_colima_profile(&env_file);
    let docker_host = docker_host(&colima_profile)?;
    log::info!("Checking Docker status");
    let docker_ready = docker_daemon_ready(&docker_host);
    // The managed Docker endpoint is the useful engine-readiness signal. A
    // Colima status call can take several seconds even when its daemon is ready.
    let engine_running = docker_ready;
    log::info!("Checking Compose service status");
    let running_services = if docker_ready {
        compose_running_services(&runtime_dir)?
    } else {
        HashSet::new()
    };
    let stack_running = !running_services.is_empty();
    let axbi_running = running_services.contains("ax-bi");
    let ports = local_runtime_ports(&urls, &running_services);
    log::info!("Checking AX BI HTTP health");
    let axbi_healthy = axbi_running && local_http_healthcheck(urls.web_port);
    let admin_password_present = configured
        && read_env_value(&env_file, "ADMIN_PASSWORD")
            .ok()
            .flatten()
            .map(|password| !password.is_empty())
            .unwrap_or(false);
    let onboarding_required =
        admin_password_present && !onboarding_complete_file(&runtime_dir).exists();

    let colima_profile = match engine {
        RuntimeEngine::Colima => colima_profile,
        RuntimeEngine::Docker => String::new(),
    };
    let can_start_local = dependencies.iter().all(|dependency| dependency.installed);
    let axbi_image = if configured {
        read_env_value(&env_file, AXBI_IMAGE_ENV)?
            .filter(|value| !value.is_empty())
            .unwrap_or_else(default_axbi_image)
    } else {
        default_axbi_image()
    };
    let ax_services_image = if configured {
        read_env_value(&env_file, AX_SERVICES_IMAGE_ENV)?
            .filter(|value| !value.is_empty())
            .unwrap_or_else(default_ax_services_image)
    } else {
        default_ax_services_image()
    };

    Ok(LocalRuntimeStatus {
        runtime_dir: path_to_string(&runtime_dir),
        compose_file: path_to_string(&compose_file),
        env_file: path_to_string(&env_file),
        configured,
        dependencies,
        ports,
        platform: platform_id().to_string(),
        local_runtime_supported: true,
        can_start_local,
        engine_name: engine.name().to_string(),
        engine_label: engine.label().to_string(),
        engine_running,
        colima_profile,
        // Keep Colima-era field name; UI uses engine_running / engine_label.
        colima_running: engine_running,
        docker_host,
        docker_ready,
        stack_running,
        axbi_running,
        axbi_healthy,
        web_url: urls.web_url,
        mcp_url: urls.mcp_url,
        services_url: urls.services_url,
        admin_username: AXBI_ADMIN_USERNAME.to_string(),
        admin_password_present,
        onboarding_required,
        desktop_version: desktop_version().to_string(),
        axbi_image,
        ax_services_image,
        recommended_cpus: AXBI_COLIMA_CPUS.parse().unwrap_or(4),
        recommended_memory_gb: AXBI_COLIMA_MEMORY_GB.parse().unwrap_or(8),
    })
}

/// Return the configured local web URL after a lightweight health validation.
///
/// The caller is already restricted to the bundled launcher, and the target
/// window has no local-runtime command privileges. Avoiding a second full
/// status scan keeps the launcher-to-web handoff responsive.
pub fn healthy_web_url<R: Runtime>(app: &AppHandle<R>) -> Result<String, String> {
    let runtime_dir = runtime_dir(app)?;
    let compose_path = compose_file(&runtime_dir);
    let env_path = env_file(&runtime_dir);
    if !compose_path.exists() || !env_path.exists() {
        return Err("Local AX BI runtime is not configured".to_string());
    }

    if !compose_running_services(&runtime_dir)?.contains("ax-bi") {
        return Err("The managed local AX BI web service is not running".to_string());
    }
    let urls = runtime_urls(&env_path);
    if !local_http_healthcheck(urls.web_port) {
        return Err("Local AX BI is not healthy yet".to_string());
    }
    Ok(urls.web_url)
}

pub fn prepare<R: Runtime>(app: &AppHandle<R>) -> Result<LocalRuntimeStatus, String> {
    ensure_runtime_files(app)?;
    status(app)
}

pub fn start<R: Runtime>(app: &AppHandle<R>) -> Result<LocalRuntimeCommandOutput, String> {
    let runtime_dir = ensure_runtime_files(app)?;
    ensure_cli_dependencies()?;
    ensure_required_ports_available(&runtime_dir)?;
    let engine_output = ensure_engine(&runtime_dir)?;
    let compose_output = start_compose_stack(&runtime_dir)?;

    Ok(LocalRuntimeCommandOutput {
        status: status(app)?,
        stdout: join_outputs(&engine_output.stdout, &compose_output.stdout),
        stderr: join_outputs(&engine_output.stderr, &compose_output.stderr),
    })
}

pub fn stop<R: Runtime>(app: &AppHandle<R>) -> Result<LocalRuntimeCommandOutput, String> {
    let runtime_dir = ensure_runtime_files(app)?;
    // Stop should still try even if the engine is flaky; surface compose errors clearly.
    let output = run_docker_compose(&runtime_dir, &["stop"])
        .map_err(|error| format_compose_error("stop", &error, Some(&runtime_dir)))?;

    Ok(LocalRuntimeCommandOutput {
        status: status(app)?,
        stdout: output.stdout,
        stderr: output.stderr,
    })
}

pub fn restart<R: Runtime>(app: &AppHandle<R>) -> Result<LocalRuntimeCommandOutput, String> {
    let runtime_dir = ensure_runtime_files(app)?;
    ensure_cli_dependencies()?;
    let engine_output = ensure_engine(&runtime_dir)?;
    let stop_output = run_docker_compose(&runtime_dir, &["stop"])
        .map_err(|error| format_compose_error("stop", &error, Some(&runtime_dir)))?;
    ensure_required_ports_available(&runtime_dir)?;
    let start_output = start_compose_stack(&runtime_dir)?;

    Ok(LocalRuntimeCommandOutput {
        status: status(app)?,
        stdout: join_outputs(
            &join_outputs(&engine_output.stdout, &stop_output.stdout),
            &start_output.stdout,
        ),
        stderr: join_outputs(
            &join_outputs(&engine_output.stderr, &stop_output.stderr),
            &start_output.stderr,
        ),
    })
}

pub fn update<R: Runtime>(app: &AppHandle<R>) -> Result<LocalRuntimeCommandOutput, String> {
    let runtime_dir = ensure_runtime_files(app)?;
    ensure_cli_dependencies()?;
    let engine_output = ensure_engine(&runtime_dir)?;
    ensure_required_ports_available(&runtime_dir)?;
    let pull_output = run_docker_compose(&runtime_dir, &["pull"])
        .map_err(|error| format_compose_error("pull", &error, Some(&runtime_dir)))?;
    let start_output = start_compose_stack(&runtime_dir)?;

    Ok(LocalRuntimeCommandOutput {
        status: status(app)?,
        stdout: join_outputs(
            &join_outputs(&engine_output.stdout, &pull_output.stdout),
            &start_output.stdout,
        ),
        stderr: join_outputs(
            &join_outputs(&engine_output.stderr, &pull_output.stderr),
            &start_output.stderr,
        ),
    })
}

pub fn logs<R: Runtime>(
    app: &AppHandle<R>,
    service: Option<String>,
    tail: Option<u16>,
) -> Result<String, String> {
    let runtime_dir = runtime_dir(app)?;
    if !compose_file(&runtime_dir).exists() || !env_file(&runtime_dir).exists() {
        return Err("Local AX BI runtime has not been prepared".to_string());
    }

    let mut args = vec![
        "logs".to_string(),
        "--no-color".to_string(),
        "--tail".to_string(),
        tail_lines(tail).to_string(),
    ];
    if let Some(service_name) = service {
        let service_name = validate_log_service(&service_name)?;
        args.push(service_name.to_string());
    }

    let output = run_docker_compose_owned(&runtime_dir, args)?;
    Ok(join_outputs(&output.stdout, &output.stderr))
}

pub fn credentials<R: Runtime>(app: &AppHandle<R>) -> Result<LocalAdminCredentials, String> {
    let env_file = env_file(&runtime_dir(app)?);
    let password = read_env_value(&env_file, "ADMIN_PASSWORD")?
        .filter(|value| !value.is_empty())
        .ok_or_else(|| {
            "Local admin credentials are not available; prepare the runtime first".to_string()
        })?;

    Ok(LocalAdminCredentials {
        username: AXBI_ADMIN_USERNAME.to_string(),
        password,
    })
}

pub fn complete_admin_onboarding<R: Runtime>(app: &AppHandle<R>) -> Result<(), String> {
    let runtime_dir = runtime_dir(app)?;
    let env_path = env_file(&runtime_dir);
    let password_present = read_env_value(&env_path, "ADMIN_PASSWORD")?
        .map(|value| !value.is_empty())
        .unwrap_or(false);
    if !password_present {
        return Err(
            "Local admin credentials are not available; prepare the runtime first".to_string(),
        );
    }

    fs::write(onboarding_complete_file(&runtime_dir), "complete\n")
        .map_err(|error| format!("Failed to save local admin onboarding state: {error}"))
}

fn ensure_runtime_files<R: Runtime>(app: &AppHandle<R>) -> Result<PathBuf, String> {
    let runtime_dir = runtime_dir(app)?;
    fs::create_dir_all(&runtime_dir)
        .map_err(|error| format!("Failed to create local runtime directory: {error}"))?;

    let compose_path = compose_file(&runtime_dir);
    let env_path = env_file(&runtime_dir);
    sync_runtime_files(&compose_path, &env_path)?;

    Ok(runtime_dir)
}

fn sync_runtime_files(compose_path: &Path, env_path: &Path) -> Result<(), String> {
    write_file_if_changed(compose_path, LOCAL_COMPOSE_YAML, "local Compose file")?;

    if !env_path.exists() {
        write_runtime_env(env_path, &build_env_file()?)?;
    } else {
        ensure_runtime_secrets(env_path)?;
        ensure_managed_image_tags(env_path)?;
        ensure_colima_profile(env_path, compose_path)?;
        secure_runtime_env_permissions(env_path)?;
    }

    Ok(())
}

/// Write `content` only when the on-disk bytes differ, via temp file + rename.
fn write_file_if_changed(path: &Path, content: &str, label: &str) -> Result<(), String> {
    if path.exists() {
        match fs::read_to_string(path) {
            Ok(existing) if existing == content => return Ok(()),
            Ok(_) | Err(_) => {}
        }
    }

    atomic_write(path, content, label)
}

/// Atomically replace `path` with `content` (write temp sibling, then rename).
fn atomic_write(path: &Path, content: &str, label: &str) -> Result<(), String> {
    let parent = path.parent().unwrap_or_else(|| Path::new("."));
    fs::create_dir_all(parent)
        .map_err(|error| format!("Failed to create parent for {label}: {error}"))?;

    let file_name = path
        .file_name()
        .and_then(|name| name.to_str())
        .unwrap_or("runtime.tmp");
    let temp_path = parent.join(format!(
        ".{file_name}.{}.tmp",
        std::process::id()
    ));

    let write_result = (|| {
        fs::write(&temp_path, content)
            .map_err(|error| format!("Failed to write temporary {label}: {error}"))?;
        fs::rename(&temp_path, path)
            .map_err(|error| format!("Failed to publish {label}: {error}"))?;
        Ok(())
    })();

    if write_result.is_err() {
        let _ = fs::remove_file(&temp_path);
    }
    write_result
}

