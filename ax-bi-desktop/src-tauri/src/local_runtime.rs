/// Write the generated runtime environment and restrict access to its secrets.
fn write_runtime_env(path: &Path, content: &str) -> Result<(), String> {
    atomic_write(path, content, "local runtime environment")?;
    secure_runtime_env_permissions(path)
}

/// Limit the runtime environment to its owning user.
fn secure_runtime_env_permissions(path: &Path) -> Result<(), String> {
    #[cfg(unix)]
    {
        use std::os::unix::fs::PermissionsExt;

        fs::set_permissions(path, fs::Permissions::from_mode(0o600)).map_err(|error| {
            format!("Failed to secure local runtime environment permissions: {error}")
        })?;
    }

    #[cfg(windows)]
    {
        secure_runtime_env_permissions_windows(path)?;
    }

    let _ = path;
    Ok(())
}

#[cfg(windows)]
fn secure_runtime_env_permissions_windows(path: &Path) -> Result<(), String> {
    let path_str = path
        .to_str()
        .ok_or_else(|| "Local runtime environment path is not valid UTF-8".to_string())?;

    // Reset inheritance and grant only the current user full control.
    let reset = Command::new("icacls")
        .args([path_str, "/inheritance:r"])
        .output()
        .map_err(|error| format!("Failed to start icacls for runtime env: {error}"))?;
    if !reset.status.success() {
        return Err(format!(
            "Failed to reset ACLs on local runtime environment: {}",
            String::from_utf8_lossy(&reset.stderr)
        ));
    }

    let username = std::env::var("USERNAME").map_err(|_| {
        "USERNAME environment variable is unset; cannot restrict runtime env ACLs on Windows"
            .to_string()
    })?;
    if username.trim().is_empty() || username.contains('%') {
        return Err(
            "USERNAME environment variable is invalid; cannot restrict runtime env ACLs on Windows"
                .to_string(),
        );
    }
    let grant = format!("{username}:F");
    let grant_out = Command::new("icacls")
        .args([path_str, "/grant:r", &grant])
        .output()
        .map_err(|error| format!("Failed to start icacls grant for runtime env: {error}"))?;
    if !grant_out.status.success() {
        return Err(format!(
            "Failed to restrict local runtime environment to the current user: {}",
            String::from_utf8_lossy(&grant_out.stderr)
        ));
    }
    Ok(())
}

/// Add or repair generated secrets in an existing runtime environment.
fn ensure_runtime_secrets(path: &Path) -> Result<(), String> {
    let content = fs::read_to_string(path)
        .map_err(|error| format!("Failed to read local runtime environment: {error}"))?;
    if let Some(updated) = ensure_runtime_secrets_content(&content)? {
        write_runtime_env(path, &updated)?;
    }
    Ok(())
}

/// Return migrated environment content when any required secret needs repair.
fn ensure_runtime_secrets_content(content: &str) -> Result<Option<String>, String> {
    let mut updated = content.to_string();
    let mut changed = false;
    for (key, byte_count) in [("AX_BI_SECRET_KEY", 48), ("AX_SERVICES_INTERNAL_TOKEN", 32)] {
        if let Some(next) = ensure_generated_secret_content(&updated, key, byte_count)? {
            updated = next;
            changed = true;
        }
    }
    Ok(changed.then_some(updated))
}

/// Ensure one environment key has exactly one non-empty generated value.
fn ensure_generated_secret_content(
    content: &str,
    key: &str,
    byte_count: usize,
) -> Result<Option<String>, String> {
    let matching_lines = content
        .lines()
        .filter(|line| {
            line.split_once('=')
                .map(|(line_key, _)| line_key.trim() == key)
                .unwrap_or(false)
        })
        .count();
    let existing = content.lines().find_map(|line| {
        let (line_key, value) = line.split_once('=')?;
        (line_key.trim() == key && !value.trim().is_empty()).then(|| value.trim().to_string())
    });
    if matching_lines == 1 && existing.is_some() {
        return Ok(None);
    }

    let value = match existing {
        Some(value) => value,
        None => random_hex(byte_count)?,
    };
    let mut lines: Vec<&str> = content
        .lines()
        .filter(|line| {
            !line
                .split_once('=')
                .map(|(line_key, _)| line_key.trim() == key)
                .unwrap_or(false)
        })
        .collect();
    while lines.last().is_some_and(|line| line.trim().is_empty()) {
        lines.pop();
    }

    let mut updated = lines.join("\n");
    if !updated.is_empty() {
        updated.push('\n');
    }
    updated.push_str(&format!("{key}={value}\n"));
    Ok(Some(updated))
}

/// Keep managed image tags and desktop version aligned with this app release.
fn ensure_managed_image_tags(path: &Path) -> Result<(), String> {
    let content = fs::read_to_string(path)
        .map_err(|error| format!("Failed to read local runtime environment: {error}"))?;
    if let Some(updated) = ensure_managed_image_tags_content(&content) {
        write_runtime_env(path, &updated)?;
    }
    Ok(())
}

/// Rewrite managed image tags in `.env` content when they are still under DEFAI control.
fn ensure_managed_image_tags_content(content: &str) -> Option<String> {
    let desired_axbi = default_axbi_image();
    let desired_services = default_ax_services_image();
    let desired_version = desktop_version().to_string();

    let mut updated = content.to_string();
    let mut changed = false;

    for (key, desired, repo) in [
        (
            AXBI_IMAGE_ENV,
            desired_axbi.as_str(),
            MANAGED_AXBI_IMAGE_REPO,
        ),
        (
            AX_SERVICES_IMAGE_ENV,
            desired_services.as_str(),
            MANAGED_AX_SERVICES_IMAGE_REPO,
        ),
    ] {
        let current = read_env_value_from_str(&updated, key).unwrap_or_default();
        if current == desired {
            continue;
        }
        if !is_managed_image_ref(&current, repo) {
            continue;
        }
        updated = upsert_env_key_content(&updated, key, desired);
        changed = true;
    }

    let current_version =
        read_env_value_from_str(&updated, AXBI_DESKTOP_VERSION_ENV).unwrap_or_default();
    if current_version != desired_version {
        updated = upsert_env_key_content(&updated, AXBI_DESKTOP_VERSION_ENV, &desired_version);
        changed = true;
    }

    changed.then_some(updated)
}

/// Insert or replace a single KEY=value line in env-file content.
fn upsert_env_key_content(content: &str, key: &str, value: &str) -> String {
    let mut lines: Vec<&str> = content
        .lines()
        .filter(|line| {
            !line
                .split_once('=')
                .map(|(line_key, _)| line_key.trim() == key)
                .unwrap_or(false)
        })
        .collect();
    while lines.last().is_some_and(|line| line.trim().is_empty()) {
        lines.pop();
    }

    let mut updated = lines.join("\n");
    if !updated.is_empty() {
        updated.push('\n');
    }
    updated.push_str(&format!("{key}={value}\n"));
    updated
}

fn ensure_colima_profile(env_path: &Path, compose_path: &Path) -> Result<(), String> {
    if !cfg!(target_os = "macos")
        || read_env_value(env_path, COLIMA_PROFILE_ENV)?
            .filter(|profile| valid_colima_profile(profile))
            .is_some()
    {
        return Ok(());
    }

    let runtime_dir = compose_path.parent().unwrap_or_else(|| Path::new("."));
    // Legacy Desktop releases used Colima's default profile. New environments
    // write `ax-bi` into .env immediately, so a missing key is an upgrade case.
    let profile = if colima_profile_has_managed_axbi(AXBI_COLIMA_PROFILE, runtime_dir) {
        AXBI_COLIMA_PROFILE
    } else {
        "default"
    };
    let mut content = fs::read_to_string(env_path)
        .map_err(|error| format!("Failed to read local runtime environment: {error}"))?;
    if !content.ends_with('\n') {
        content.push('\n');
    }
    content.push_str(&format!("{COLIMA_PROFILE_ENV}={profile}\n"));
    write_runtime_env(env_path, &content)
}

fn configured_colima_profile(env_path: &Path) -> String {
    read_env_value(env_path, COLIMA_PROFILE_ENV)
        .ok()
        .flatten()
        .filter(|profile| valid_colima_profile(profile))
        .unwrap_or_else(|| AXBI_COLIMA_PROFILE.to_string())
}

fn valid_colima_profile(profile: &str) -> bool {
    !profile.is_empty()
        && profile
            .chars()
            .all(|character| character.is_ascii_alphanumeric() || matches!(character, '-' | '_'))
}

fn colima_profile_has_managed_axbi(profile: &str, runtime_dir: &Path) -> bool {
    let Ok(host) = docker_host(profile) else {
        return false;
    };
    if !docker_daemon_ready(&host) {
        return false;
    }

    let project_filter = format!("label=com.docker.compose.project={AXBI_COMPOSE_PROJECT}");
    let service_filter = "label=com.docker.compose.service=ax-bi";
    let runtime_filter = format!(
        "label=com.docker.compose.project.working_dir={}",
        path_to_string(runtime_dir)
    );
    run_command(
        "docker",
        &[
            "ps",
            "--all",
            "--filter",
            &project_filter,
            "--filter",
            service_filter,
            "--filter",
            &runtime_filter,
            "--format",
            "{{.ID}}",
        ],
        None,
        &[("DOCKER_HOST", &host)],
    )
    .map(|output| !output.stdout.trim().is_empty())
    .unwrap_or(false)
}

fn random_hex(byte_count: usize) -> Result<String, String> {
    let mut bytes = vec![0; byte_count];
    getrandom::fill(&mut bytes)
        .map_err(|error| format!("Failed to read secure random source: {error}"))?;
    Ok(bytes
        .iter()
        .map(|byte| format!("{byte:02x}"))
        .collect::<Vec<_>>()
        .join(""))
}

fn dependencies() -> Vec<LocalRuntimeDependency> {
    LOCAL_RUNTIME_DEPENDENCIES
        .get_or_init(detect_dependencies)
        .clone()
}

/// Detect independent CLI dependencies concurrently and cache them per launch.
fn detect_dependencies() -> Vec<LocalRuntimeDependency> {
    let join_checks = |handles: Vec<thread::ScopedJoinHandle<'_, LocalRuntimeDependency>>| {
        handles
            .into_iter()
            .map(|handle| {
                handle.join().unwrap_or_else(|_| LocalRuntimeDependency {
                    name: "Runtime dependency".to_string(),
                    command: "unknown".to_string(),
                    resolved_path: String::new(),
                    installed: false,
                    version: None,
                    install_hint: "Restart AX BI Desktop and retry dependency detection"
                        .to_string(),
                })
            })
            .collect()
    };

    match RuntimeEngine::current() {
        RuntimeEngine::Colima => thread::scope(|scope| {
            join_checks(vec![
                scope.spawn(|| {
                    dependency("Colima", "colima", &["--version"], "brew install colima")
                }),
                scope.spawn(|| dependency("Lima", "limactl", &["--version"], "brew install lima")),
                scope.spawn(|| {
                    dependency(
                        "Docker CLI",
                        "docker",
                        &["--version"],
                        "brew install docker",
                    )
                }),
                scope.spawn(|| {
                    dependency(
                        "Docker Compose",
                        "docker",
                        &["compose", "version"],
                        "brew install docker-compose",
                    )
                }),
            ])
        }),
        RuntimeEngine::Docker => thread::scope(|scope| {
            join_checks(vec![
                scope.spawn(|| {
                    dependency(
                        "Docker CLI",
                        "docker",
                        &["--version"],
                        docker_install_hint(),
                    )
                }),
                scope.spawn(|| {
                    dependency(
                        "Docker Compose",
                        "docker",
                        &["compose", "version"],
                        docker_install_hint(),
                    )
                }),
            ])
        }),
    }
}

fn docker_install_hint() -> &'static str {
    if cfg!(target_os = "windows") {
        "Install Docker Desktop (winget install -e --id Docker.DockerDesktop) or from https://docs.docker.com/desktop/setup/install/windows-install/ and ensure it is running"
    } else if cfg!(target_os = "macos") {
        "brew install docker docker-compose"
    } else {
        "Install Docker Engine and the Compose plugin for your distribution"
    }
}

