fn local_runtime_port(
    name: &str,
    service: &str,
    port: u16,
    url: &str,
    running_services: &HashSet<String>,
) -> LocalRuntimePort {
    let open = tcp_port_open(port);
    local_runtime_port_from_state(name, port, url, open, running_services.contains(service))
}

/// Derive port availability from listener and managed-service state.
fn local_runtime_port_from_state(
    name: &str,
    port: u16,
    url: &str,
    open: bool,
    managed_service_running: bool,
) -> LocalRuntimePort {
    let occupied_by_axbi = open && managed_service_running;
    LocalRuntimePort {
        name: name.to_string(),
        port,
        url: url.to_string(),
        available: !open || occupied_by_axbi,
        occupied_by_axbi,
    }
}

fn ensure_required_ports_available(runtime_dir: &Path) -> Result<(), String> {
    let urls = runtime_urls(&env_file(runtime_dir));
    let running_services = compose_running_services(runtime_dir).unwrap_or_default();
    let blocked: Vec<LocalRuntimePort> = local_runtime_ports(&urls, &running_services)
        .into_iter()
        .filter(|port| !port.available)
        .collect();
    if blocked.is_empty() {
        return Ok(());
    }

    let details = blocked
        .iter()
        .map(|port| {
            let owner = describe_port_owner(runtime_dir, port.port)
                .map(|owner| format!(" — {owner}"))
                .unwrap_or_default();
            format!("- {} port {} ({}){owner}", port.name, port.port, port.url)
        })
        .collect::<Vec<_>>()
        .join("\n");
    Err(format!(
        "Cannot start local AX BI because required port(s) are already in use:\n{}\n\n\
         Stop the other service or change the AX BI ports in the local runtime settings.",
        details
    ))
}

/// Identify a Docker container or host process listening on a blocked port.
fn describe_port_owner(runtime_dir: &Path, port: u16) -> Option<String> {
    if let Ok(host) = docker_host(&configured_colima_profile(&env_file(runtime_dir))) {
        let envs = docker_host_envs(&host);
        let env_refs: Vec<(&str, &str)> = envs
            .iter()
            .map(|(key, value)| (key.as_str(), value.as_str()))
            .collect();
        let port_filter = format!("publish={port}");
        if let Ok(output) = run_command(
            "docker",
            &[
                "ps",
                "--filter",
                &port_filter,
                "--format",
                "{{.Names}} (Docker container {{.ID}})",
            ],
            None,
            &env_refs,
        ) {
            if let Some(owner) = first_line(&output.stdout) {
                return Some(owner);
            }
        }
    }
    host_port_owner(port)
}

/// Identify the first host process listening on a TCP port.
#[cfg(unix)]
fn host_port_owner(port: u16) -> Option<String> {
    let port_selector = format!("-iTCP:{port}");
    let output = run_command(
        "lsof",
        &["-nP", &port_selector, "-sTCP:LISTEN", "-Fpc"],
        None,
        &[],
    )
    .ok()?;
    parse_lsof_port_owner(&output.stdout)
}

/// Parse `lsof -Fpc` output into a concise process description.
#[cfg(unix)]
fn parse_lsof_port_owner(output: &str) -> Option<String> {
    let pid = output.lines().find_map(|line| line.strip_prefix('p'))?;
    let command = output
        .lines()
        .find_map(|line| line.strip_prefix('c'))
        .unwrap_or("process");
    Some(format!("{command} (PID {pid})"))
}

/// Identify the first Windows process listening on a TCP port.
#[cfg(windows)]
fn host_port_owner(port: u16) -> Option<String> {
    let output = run_command("netstat", &["-ano", "-p", "TCP"], None, &[]).ok()?;
    let port_suffix = format!(":{port}");
    output.stdout.lines().find_map(|line| {
        let fields: Vec<&str> = line.split_whitespace().collect();
        (fields.len() >= 5
            && fields[1].ends_with(&port_suffix)
            && fields[3].eq_ignore_ascii_case("LISTENING"))
        .then(|| format!("Windows process PID {}", fields[4]))
    })
}

fn tcp_port_open(port: u16) -> bool {
    tcp_socket_addr(port)
        .and_then(|addr| TcpStream::connect_timeout(&addr, Duration::from_millis(250)).ok())
        .is_some()
}

fn tcp_socket_addr(port: u16) -> Option<SocketAddr> {
    (AXBI_HEALTH_HOST, port)
        .to_socket_addrs()
        .ok()
        .and_then(|mut addresses| addresses.next())
}

fn runtime_urls(env_path: &Path) -> RuntimeUrls {
    let web_port = read_env_port(env_path, "AXBI_PORT", AXBI_HEALTH_PORT);
    let mcp_port = read_env_port(env_path, "MCP_PORT", AXBI_MCP_PORT);
    let services_port = read_env_port(env_path, "AX_SERVICES_PORT", AXBI_SERVICES_PORT);

    RuntimeUrls {
        web_port,
        mcp_port,
        services_port,
        web_url: format!("http://127.0.0.1:{web_port}/ax-bi/welcome/"),
        mcp_url: format!("http://127.0.0.1:{mcp_port}/mcp"),
        services_url: format!("http://127.0.0.1:{services_port}"),
    }
}

fn read_env_port(env_path: &Path, key: &str, default: u16) -> u16 {
    read_env_value(env_path, key)
        .ok()
        .flatten()
        .and_then(|value| value.parse::<u16>().ok())
        .unwrap_or(default)
}

fn run_command(
    program: &str,
    args: &[&str],
    cwd: Option<&Path>,
    envs: &[(&str, &str)],
) -> Result<CommandOutput, String> {
    run_command_with_timeout(program, args, cwd, envs, COMMAND_TIMEOUT)
}

fn run_command_with_timeout(
    program: &str,
    args: &[&str],
    cwd: Option<&Path>,
    envs: &[(&str, &str)],
    timeout: Duration,
) -> Result<CommandOutput, String> {
    let program_path = resolve_program(program);
    let mut command = Command::new(&program_path);
    command.args(args.iter().map(OsStr::new));
    if let Some(cwd) = cwd {
        command.current_dir(cwd);
    }
    command.stdin(Stdio::null());
    command.stdout(Stdio::piped());
    command.stderr(Stdio::piped());
    configure_subprocess(&mut command);
    apply_env_pairs(&mut command, envs);

    let child = command.spawn().map_err(|error| {
        format!(
            "Failed to run {program} (resolved as {}): {error}",
            program_path.display()
        )
    })?;
    let pid = child.id();

    let (tx, rx) = mpsc::channel();
    thread::spawn(move || {
        let result = child.wait_with_output();
        let _ = tx.send(result);
    });

    let output = match rx.recv_timeout(timeout) {
        Ok(Ok(output)) => output,
        Ok(Err(error)) => {
            return Err(format!(
                "Failed to run {program} (resolved as {}): {error}",
                program_path.display()
            ));
        }
        Err(mpsc::RecvTimeoutError::Timeout) => {
            kill_process_tree(pid);
            // Drain the waiter so the OS reaps the process.
            let _ = rx.recv_timeout(Duration::from_secs(2));
            return Err(format!(
                "{program} timed out after {}s and was terminated",
                timeout.as_secs()
            ));
        }
        Err(mpsc::RecvTimeoutError::Disconnected) => {
            kill_process_tree(pid);
            return Err(format!(
                "Failed to run {program}: subprocess waiter disconnected"
            ));
        }
    };

    let stdout = String::from_utf8_lossy(&output.stdout).to_string();
    let stderr = String::from_utf8_lossy(&output.stderr).to_string();

    if !output.status.success() {
        return Err(format!(
            "{program} exited with status {}{}{}",
            output.status,
            format_output("stdout", &stdout),
            format_output("stderr", &stderr)
        ));
    }

    Ok(CommandOutput { stdout, stderr })
}

/// Best-effort kill of a hung subprocess (and children on Windows).
fn kill_process_tree(pid: u32) {
    if pid == 0 {
        return;
    }

    #[cfg(unix)]
    {
        let _ = Command::new("kill")
            .args(["-TERM", &pid.to_string()])
            .stdout(Stdio::null())
            .stderr(Stdio::null())
            .status();
        thread::sleep(Duration::from_millis(200));
        let _ = Command::new("kill")
            .args(["-KILL", &pid.to_string()])
            .stdout(Stdio::null())
            .stderr(Stdio::null())
            .status();
    }

    #[cfg(windows)]
    {
        let _ = Command::new("taskkill")
            .args(["/PID", &pid.to_string(), "/T", "/F"])
            .stdout(Stdio::null())
            .stderr(Stdio::null())
            .status();
    }
}

/// Shared subprocess setup for GUI apps: fixed PATH + no console flash on Windows.
fn configure_subprocess(command: &mut Command) {
    apply_augmented_path(command);
    #[cfg(target_os = "windows")]
    {
        use std::os::windows::process::CommandExt;
        // CREATE_NO_WINDOW — avoid flashing consoles when spawning docker/colima.
        const CREATE_NO_WINDOW: u32 = 0x0800_0000;
        command.creation_flags(CREATE_NO_WINDOW);
    }
}

fn apply_env_pairs(command: &mut Command, envs: &[(&str, &str)]) {
    for (key, value) in envs {
        if *key == "DOCKER_HOST" && value.is_empty() {
            command.env_remove("DOCKER_HOST");
        } else {
            command.env(key, value);
        }
    }
}

/// Resolve Homebrew / Docker Desktop binaries for GUI-launched applications.
///
/// Finder, Dock, and Windows shortcuts do not inherit the user's interactive
/// shell PATH, so known install prefixes are checked before PATH lookup.
fn resolve_program(program: &str) -> PathBuf {
    for dir in known_bin_dirs() {
        let candidate = dir.join(program_filename(program));
        if candidate.is_file() {
            return candidate;
        }
    }

    if let Some(from_path) = which_on_augmented_path(program) {
        return from_path;
    }

    PathBuf::from(program_filename(program))
}

fn program_filename(program: &str) -> String {
    if cfg!(target_os = "windows") && !program.ends_with(".exe") {
        format!("{program}.exe")
    } else {
        program.to_string()
    }
}

fn which_on_augmented_path(program: &str) -> Option<PathBuf> {
    let name = program_filename(program);
    let path_var = augmented_path();
    for dir in std::env::split_paths(&path_var) {
        let candidate = dir.join(&name);
        if candidate.is_file() {
            return Some(candidate);
        }
    }
    None
}

fn known_bin_dirs() -> Vec<PathBuf> {
    let mut dirs = Vec::new();

    #[cfg(target_os = "macos")]
    {
        dirs.push(PathBuf::from("/opt/homebrew/bin"));
        dirs.push(PathBuf::from("/opt/homebrew/sbin"));
        dirs.push(PathBuf::from("/usr/local/bin"));
        dirs.push(PathBuf::from("/usr/local/sbin"));
        dirs.push(PathBuf::from("/usr/sbin"));
    }

    #[cfg(target_os = "windows")]
    {
        if let Ok(program_files) = std::env::var("ProgramFiles") {
            dirs.push(
                PathBuf::from(&program_files)
                    .join("Docker")
                    .join("Docker")
                    .join("resources")
                    .join("bin"),
            );
            // Newer Docker Desktop installs also expose CLI next to the app.
            dirs.push(PathBuf::from(&program_files).join("Docker").join("Docker"));
            dirs.push(
                PathBuf::from(&program_files)
                    .join("Docker")
                    .join("Docker")
                    .join("resources")
                    .join("cli-plugins"),
            );
        }
        if let Ok(program_files_x86) = std::env::var("ProgramFiles(x86)") {
            dirs.push(
                PathBuf::from(program_files_x86)
                    .join("Docker")
                    .join("Docker")
                    .join("resources")
                    .join("bin"),
            );
        }
        if let Ok(local_app_data) = std::env::var("LOCALAPPDATA") {
            dirs.push(
                PathBuf::from(&local_app_data)
                    .join("Docker")
                    .join("resources")
                    .join("bin"),
            );
            dirs.push(
                PathBuf::from(local_app_data)
                    .join("Programs")
                    .join("Docker")
                    .join("Docker")
                    .join("resources")
                    .join("bin"),
            );
        }
        // User PATH often includes this after Docker Desktop install.
        if let Ok(user_profile) = std::env::var("USERPROFILE") {
            dirs.push(
                PathBuf::from(user_profile)
                    .join("AppData")
                    .join("Local")
                    .join("Docker")
                    .join("resources")
                    .join("bin"),
            );
        }
    }

    #[cfg(target_os = "linux")]
    {
        dirs.push(PathBuf::from("/usr/local/bin"));
        dirs.push(PathBuf::from("/usr/bin"));
        dirs.push(PathBuf::from("/usr/sbin"));
        if let Ok(home) = std::env::var("HOME") {
            dirs.push(PathBuf::from(home).join(".local").join("bin"));
        }
    }

    dirs
}

/// PATH that includes known install prefixes so child tools (e.g. Colima → limactl)
/// resolve correctly when the desktop app is launched without a login shell.
fn augmented_path() -> String {
    let mut parts: Vec<PathBuf> = known_bin_dirs();
    if let Some(existing) = std::env::var_os("PATH") {
        parts.extend(std::env::split_paths(&existing));
    }
    // Deduplicate while preserving order.
    let mut seen = std::collections::HashSet::new();
    parts.retain(|p| seen.insert(p.clone()));
    std::env::join_paths(parts)
        .map(|os| os.to_string_lossy().into_owned())
        .unwrap_or_else(|_| std::env::var("PATH").unwrap_or_default())
}

fn apply_augmented_path(command: &mut Command) {
    command.env("PATH", augmented_path());
}

fn format_output(label: &str, value: &str) -> String {
    if value.trim().is_empty() {
        String::new()
    } else {
        format!("\n{label}:\n{}", value.trim())
    }
}

fn docker_host(colima_profile: &str) -> Result<String, String> {
    match RuntimeEngine::current() {
        RuntimeEngine::Colima => {
            let home = home_dir()?;
            Ok(format!(
                "unix://{}/.colima/{colima_profile}/docker.sock",
                path_to_string(&home)
            ))
        }
        // Pin the local engine endpoint so a stale inherited DOCKER_HOST cannot break us.
        RuntimeEngine::Docker => {
            if cfg!(target_os = "windows") {
                Ok("npipe:////./pipe/docker_engine".to_string())
            } else {
                Ok("unix:///var/run/docker.sock".to_string())
            }
        }
    }
}

fn docker_host_envs(docker_host: &str) -> Vec<(String, String)> {
    // Always set DOCKER_HOST for managed runtime commands (including platform default).
    vec![("DOCKER_HOST".to_string(), docker_host.to_string())]
}

fn home_dir() -> Result<PathBuf, String> {
    if let Ok(home) = std::env::var("HOME") {
        return Ok(PathBuf::from(home));
    }
    #[cfg(target_os = "windows")]
    if let Ok(profile) = std::env::var("USERPROFILE") {
        return Ok(PathBuf::from(profile));
    }
    Err("Home directory is not set (HOME / USERPROFILE)".to_string())
}

/// Path to Docker Desktop app when installed (Windows; optional macOS fallback).
fn docker_desktop_app_path() -> Option<PathBuf> {
    let mut candidates = Vec::new();

    #[cfg(target_os = "windows")]
    {
        if let Ok(program_files) = std::env::var("ProgramFiles") {
            candidates.push(
                PathBuf::from(program_files)
                    .join("Docker")
                    .join("Docker")
                    .join("Docker Desktop.exe"),
            );
        }
        if let Ok(program_files_x86) = std::env::var("ProgramFiles(x86)") {
            candidates.push(
                PathBuf::from(program_files_x86)
                    .join("Docker")
                    .join("Docker")
                    .join("Docker Desktop.exe"),
            );
        }
        if let Ok(local_app_data) = std::env::var("LOCALAPPDATA") {
            candidates.push(
                PathBuf::from(local_app_data)
                    .join("Docker")
                    .join("Docker Desktop.exe"),
            );
        }
    }

    #[cfg(target_os = "macos")]
    {
        candidates.push(PathBuf::from(
            "/Applications/Docker.app/Contents/MacOS/Docker",
        ));
    }

    candidates.into_iter().find(|path| path.is_file())
}

fn local_http_healthcheck(port: u16) -> bool {
    let Ok(mut addresses) = (AXBI_HEALTH_HOST, port).to_socket_addrs() else {
        return false;
    };
    let Some(address) = addresses.next() else {
        return false;
    };
    let Ok(mut stream) = TcpStream::connect_timeout(&address, Duration::from_millis(750)) else {
        return false;
    };
    let _ = stream.set_read_timeout(Some(Duration::from_millis(750)));
    let _ = stream.set_write_timeout(Some(Duration::from_millis(750)));

    if stream
        .write_all(b"GET /health HTTP/1.1\r\nHost: 127.0.0.1\r\nConnection: close\r\n\r\n")
        .is_err()
    {
        return false;
    }

    let mut response = String::new();
    stream.read_to_string(&mut response).is_ok()
        && (response.starts_with("HTTP/1.1 200") || response.starts_with("HTTP/1.0 200"))
}

fn validate_log_service(service: &str) -> Result<&str, String> {
    let service = service.trim();
    if ALLOWED_LOG_SERVICES.contains(&service) {
        Ok(service)
    } else {
        Err("Unknown local AX BI service name".to_string())
    }
}

fn tail_lines(value: Option<u16>) -> u16 {
    value.unwrap_or(200).clamp(1, MAX_LOG_TAIL_LINES)
}

fn read_env_value(path: &Path, key: &str) -> Result<Option<String>, String> {
    let content = fs::read_to_string(path)
        .map_err(|error| format!("Failed to read local runtime environment: {error}"))?;
    Ok(read_env_value_from_str(&content, key))
}

fn read_env_value_from_str(content: &str, key: &str) -> Option<String> {
    content.lines().find_map(|line| {
        let trimmed_line = line.trim();
        if trimmed_line.is_empty() || trimmed_line.starts_with('#') {
            return None;
        }
        let (line_key, line_value) = trimmed_line.split_once('=')?;
        if line_key.trim() == key {
            Some(strip_env_value_quotes(line_value.trim()).to_string())
        } else {
            None
        }
    })
}

/// Strip a single pair of matching surrounding quotes from an env value.
fn strip_env_value_quotes(value: &str) -> &str {
    let bytes = value.as_bytes();
    if bytes.len() >= 2 {
        let first = bytes[0];
        let last = bytes[bytes.len() - 1];
        if (first == b'"' && last == b'"') || (first == b'\'' && last == b'\'') {
            return &value[1..value.len() - 1];
        }
    }
    value
}

fn join_outputs(first: &str, second: &str) -> String {
    match (first.trim(), second.trim()) {
        ("", "") => String::new(),
        (first, "") => first.to_string(),
        ("", second) => second.to_string(),
        (first, second) => format!("{first}\n{second}"),
    }
}

fn path_to_string(path: &Path) -> String {
    path.to_string_lossy().to_string()
}

